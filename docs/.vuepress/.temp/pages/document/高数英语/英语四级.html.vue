<p>2）一般过去时</p>
<p>主动语态：did</p>
<p>被动语态：was/were + done</p>
<p>The window was broken by my son.</p>
<p>3）一般将来时</p>
<p>主动语态：will/shall do</p>
<p>被动语态：will//shall + be done</p>
