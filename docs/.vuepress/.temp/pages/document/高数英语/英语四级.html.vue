<p>主动语态：would do</p>
<p>被动语态：would be done</p>
<p>5）现在进行时</p>
<p>主动语态：am/is/are doing</p>
<p>被动语态：am/is/are being done</p>
<p>6）过去进行时</p>
<p>主动语态：was/were doing</p>
<p>被动语态：was/were being done</p>
<p>7）现在完成时</p>
<p>主动语态：has/have done</p>
<p>被动语态：has/have been done</p>
<p>8）过去完成时</p>
<p>主动语态：had done</p>
<p>被动语态：had been done</p>
<p>9）将来完成时</p>
<p>主动语态：will/shall have done</p>
<p>被动语态：will/shall have been done</p>
<p>10）过去将来完成时</p>
<p>主动语态：would have done</p>
