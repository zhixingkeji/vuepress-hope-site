<ol>
<li>
<p>动词概述</p>
<p>动词是用来形容或表示各类动作及状态的词，每个句子只能有一个动词</p>
</li>
<li>
<p>系动词</p>
</li>
</ol>
<p>1）系动词概念：系动词也叫连系动词，有些具有词义，有些不具有，不能单独作谓语。</p>
<p>2）系动词用法</p>
<ul>
<li>
<p>状态系动词用来表示主语的性质或状态， 主要有 be（am、is、are）</p>
</li>
<li>
<p>持续系动词表示主语持续保持一种状态，主要有 keep、rest、remain、stay</p>
</li>
<li>
<p>变化系动词表示主语变成什么样，主要有 become、grow、get、go、run、come</p>
</li>
<li>
<p>感官系统词表示主语的感受，主要有feel、smell、sound、taste</p>
</li>
<li>
<p>表像系动词表示看起来像，主要有seem、appear、look</p>
</li>
<li>
<p>终止系动词表示主语已经停止动作，主要有prove，turn out</p>
</li>
</ul>
<ol start="3">
<li>情态动词</li>
</ol>
<p>1）情态动词概念：表示人说话的语气或者情态的动词</p>
<p>2）情态动词类型</p>
<p>can（能、会），may（可以），must（必须），need（需要），should（应该）</p>
<ol start="4">
<li>助动词</li>
</ol>
<p>助动词用来帮助主要动词完成语法时态的动词。</p>
<p>常用的有：be、have、do、shall、will、should、would</p>
<ol start="5">
<li>实义动词</li>
</ol>
<p>及物动词（后面必须加宾语），不及物动词（后面不能加宾语）</p>
