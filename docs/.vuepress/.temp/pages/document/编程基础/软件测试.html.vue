<p>（2）依据</p>
<p>验收合同，用户的原始需求</p>
<p>（3）方法</p>
<p>黑盒测试方法</p>
<p>（4）考察基准</p>
<p>原始需求覆盖率</p>
<p>（5）英文</p>
<p>User Acceptance Testing（UAT）</p>
<p>（6）补充</p>
<p>Alpha测试（内测）</p>
