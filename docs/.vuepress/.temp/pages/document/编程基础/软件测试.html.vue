<p>（4）考察基准</p>
<p>需求覆盖率</p>
<p>（5）英文</p>
<p>System Testing（ST）</p>
<p>（6）补充</p>
<p>常见的系统测试类型：功能测试，性能测试，安全测试，易用性测试</p>
