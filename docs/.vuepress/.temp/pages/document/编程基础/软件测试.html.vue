<h3 id="_5-1-单元测试" tabindex="-1"><a class="header-anchor" href="#_5-1-单元测试" aria-hidden="true">#</a> 5.1 单元测试</h3>
<p>（1）概念</p>
<p>针对组成软件系统的最基本的单位（<strong>函数或者类</strong>）进行测试，叫做单元测试，也称组件测试。</p>
<p>（2）测试依据</p>
<p>详细设计说明书（LLD）</p>
<p>（3）方法</p>
<p>白盒测试方法为主</p>
<p>（4）考察基准</p>
<p>逻辑覆盖率</p>
<p>（5）英文</p>
<p>Unit Testing（UT）或Component Testing（组件测试）</p>
<p>（6）补充</p>
<p>孤立的测试策略：自顶向下，自底向上</p>
<h3 id="_5-2-集成测试" tabindex="-1"><a class="header-anchor" href="#_5-2-集成测试" aria-hidden="true">#</a> 5.2 集成测试</h3>
