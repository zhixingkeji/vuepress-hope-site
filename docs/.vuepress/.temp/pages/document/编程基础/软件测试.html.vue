<h2 id="第8章-测试方法及分类" tabindex="-1"><a class="header-anchor" href="#第8章-测试方法及分类" aria-hidden="true">#</a> 第8章 测试方法及分类</h2>
<h3 id="_8-1-黑盒测试和白盒测试" tabindex="-1"><a class="header-anchor" href="#_8-1-黑盒测试和白盒测试" aria-hidden="true">#</a> 8.1 黑盒测试和白盒测试</h3>
<p>（1）白盒测试（White Box Testing）</p>
<p>①概念：针对软件系统的内部细节做测试，考察内部的逻辑结构，好比盒子是开发的或者透明的，所以叫做白盒测试。（Open Box Testing；Glass Box Testing；Logic Driven Testing）</p>
<p>②白盒的静态测试：控制流分析，数据流分析，信息流分析</p>
<p>③白盒的动态测试：程序插装与逻辑覆盖率</p>
<p>【解析】</p>
<p>程序插装：将测试代码插入到被测试代码，通过运行插入测试代码的程序得出测试结果，查出被测试代码中是否有问题。</p>
<p>逻辑覆盖率：语句覆盖率；判定覆盖率；条件覆盖率；路径覆盖率</p>
<p>④白盒测试特点</p>
<p>优点：问题发生容易定位</p>
<p>​      测试的比较彻底</p>
<p>​      解决问题的成本比较低</p>
<p>缺点：对测试人员的代码能力要求高</p>
<p>​      测试的工作的工作量大</p>
<p>​      测试的成本比较高</p>
<p>​      对规格问题不做验证</p>
<p>（2）黑盒测试（Black Box Testing）</p>
<p>①概念：</p>
<p>针对软件系统的整体规格做测试，看不到内部的细节结构，好比一个黑色的盒子，所以叫黑盒测试</p>
<p>②黑盒的方法</p>
<p>等价类划分法；边界值分析法；判定表法；因果图法；正交实验法；状态迁移图法；流程分析法</p>
<p>③常见的黑盒测试类型</p>
<p>功能测试；性能测试；负载测试；容量测试</p>
<p>④黑盒测试特点</p>
<p>优点：对代码能力要求低，只测试最终的贵，测试效率高，容易理解</p>
<p>缺点：解决问题的成本高，问题定位比较难，不测试软件系统内部细节逻辑结果，对软件需求规格说明书的要求比较高</p>
<p>（3）灰盒测试</p>
<p>①概念：综合运用黑盒测试与白盒测试，灰度取决于被测试对象的颗粒度</p>
<h3 id="_8-2-静态测试和动态测试" tabindex="-1"><a class="header-anchor" href="#_8-2-静态测试和动态测试" aria-hidden="true">#</a> 8.2 静态测试和动态测试</h3>
<p>（1）静态测试</p>
<p>①概念</p>
<p>被测试对象没有被运行（比如代码的编译，文档的评审），主要是针对软件生产过程中的中间产品</p>
<p>②自动化手段（静态分析，语法分析，符号执行）</p>
<p>③人工手段（评审：代码走读；设计文档技术评审；需求规格说明书的正规检视）</p>
<p>④正规检视：必须严格遵守流程规范（角色分配；活动安排），目的是为了发现缺陷</p>
<p>⑤技术评审：对技术文档进行评估，选择方案</p>
<p>⑥走读：形式比较随意，作者自己讲解，目的是为了发现缺陷，同时可以交流学习</p>
<p>（2）动态测试</p>
<p>概念：被测试对象被运行</p>
<h3 id="_8-3-自动化测试和人工测试" tabindex="-1"><a class="header-anchor" href="#_8-3-自动化测试和人工测试" aria-hidden="true">#</a> 8.3 自动化测试和人工测试</h3>
<p>（1）人工测试</p>
<p>需要智能的，执行一次的</p>
<p>（2）自动化测试</p>
<p>机械的重复的，不需要智能的，人工无法完成的（大并发量，查看微妙级的时间相应的）</p>
